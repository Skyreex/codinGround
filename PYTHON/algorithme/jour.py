a = int(input("n : "))
if a > 0 and a < 8:
    if a == 1:
        print("Lundi")
    elif a == 2:
        print("Mardi")
    elif a == 3:
        print("Mercredi")
    elif a == 4:
        print("Jeudi")
    elif a == 5:
        print("Vendredi")
    elif a == 6:
        print("Samedi")
    elif a == 7:
        print("Dimanche")
else:
    print("error")
